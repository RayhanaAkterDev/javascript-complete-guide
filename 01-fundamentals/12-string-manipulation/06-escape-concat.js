/*
  Topic: 06 - Escape Characters and Concatenation

  Includes:
    - Newline and quote escaping
    - String concatenation using +
*/

let quote = "He said, \"JavaScript is awesome!\"";
console.log("Escaped quotes:", quote);

// --- Newline escape ---
let multiline = "Line1\nLine2\nLine3";
console.log("With newlines:\n" + multiline);

// --- String concatenation ---
let name = "Sumaya";
let greeting = "Hello, " + name + "!";
console.log("Concatenated:", greeting);
