/*
  Topic: 01 - Basics of String Declaration

  Demonstrates how to declare strings in JavaScript using:
    - Single quotes
    - Double quotes
    - Backticks (template literals)
    - typeof operator
*/

// --- Single quote string ---
let singleQuote = 'Hello from single quotes';
console.log(singleQuote);

// --- Double quote string ---
let doubleQuote = "Hello from double quotes";
console.log(doubleQuote);

// --- Backtick string (template literal) ---
let backtickQuote = `Hello from backticks`;
console.log(backtickQuote);

// --- typeof check ---
console.log(typeof singleQuote);  // "string"
console.log(typeof backtickQuote); // "string"
